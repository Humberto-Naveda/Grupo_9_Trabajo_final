private void cargarTabla() {
    DefaultTableModel modelo = new DefaultTableModel(
        new String[]{"ID", "Nombre", "DNI", "Fecha Nac"}, 0
    );

    for (Comprador c : cd.listarCompradores()) {   
        modelo.addRow(new Object[]{
            c.getIdComprador(),
            c.getNombre(),
            c.getDni(),
            c.getFechaNac()
        });
    }

    tableCompradores.setModel(modelo);
}

