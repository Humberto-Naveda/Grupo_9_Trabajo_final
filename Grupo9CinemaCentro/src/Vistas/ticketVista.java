    private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbguardarActionPerformed
        Ticket t;
        if (txtfechacompra.getDate() == null || txtfechafuncion.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar ambas fechas ");
            return;
        }
        if (txtmonto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el monto del ticket.");
            return;
        }
      

        try{
        Comprador c=(Comprador) txtcomprador.getSelectedItem();
        Lugar l=(Lugar) txtlugar.getSelectedItem();
        int id=Integer.parseInt(txtid.getText());
        LocalDate fechac=txtfechacompra.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate fechaf=txtfechafuncion.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        Double monto=Double.parseDouble(txtmonto.getText());
        boolean activo=txtactivo.isSelected();
        t=new Ticket(id,l,c,fechac,fechaf,monto,activo);
        
        td.guardarTicket(t);
        
        }catch(NumberFormatException r){
        JOptionPane.showMessageDialog(null, "debe ingresar un valor numerico");
        
        }
        
    }//GEN-LAST:event_jbguardarActionPerformed

    private void jbbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbbuscarActionPerformed
txtid.setEnabled(true);
        if(txtid.getText().isEmpty()){
        
      JOptionPane.showMessageDialog(null, "ingrese el id del ticket que desea buscar");
        return;
        }
        try{
        
        int id=Integer.parseInt(txtid.getText());
        
        
       Ticket t= td.buscarTicket(id);
       
       txtmonto.setText(String.valueOf(t.getMonto()));
            LocalDate pe= t.getFechaFuncion();
        
              java.util.Date date = java.util.Date.from(pe.atStartOfDay(ZoneId.systemDefault()).toInstant());
     txtfechafuncion.setDate(date);
     
     LocalDate po= t.getFechaCompra();
        
              java.util.Date dat = java.util.Date.from(po.atStartOfDay(ZoneId.systemDefault()).toInstant());
     txtfechacompra.setDate(dat);
  
            txtcomprador.setSelectedItem(t.getComprador());
            txtlugar.setSelectedItem(t.getAsiento());
           txtactivo.setSelected(t.isActivo());
          
       }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "debe ingresar un valor numerico");
        }
    }//GEN-LAST:event_jbbuscarActionPerformed

    private void jbanularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbanularActionPerformed
      txtid.setEnabled(true);
        if(txtid.getText().isEmpty()){
       JOptionPane.showMessageDialog(null, "debe ingresar el id del ticket que desea anular");
      return;
      }
      try{  
        int id=Integer.parseInt(txtid.getText());
        td.anularTicket(id);
        limpiarcampo();
        txtid.setEnabled(false);
      }catch(NumberFormatException e){
      JOptionPane.showMessageDialog(null, "debe ingresar un valor numerico");
      
      }
    }//GEN-LAST:event_jbanularActionPerformed

