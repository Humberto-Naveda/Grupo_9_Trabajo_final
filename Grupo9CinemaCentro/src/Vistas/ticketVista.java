public void llenarCombo(){
List<Comprador>compradores= cd.listarCompradores();
for(Comprador c:compradores){
txtcomprador.addItem(c);

}
List<Proyeccion> proyecciones= pd.listarProyeccion();
for(Proyeccion pro:proyecciones){
    txtProyeccion.addItem(pro);
}
}
public void cargarlugares(int id){
   
    txtlugar.removeAllItems();
List<Lugar>lugares=ld.lugaresDisponiblesPorProyeccion(id);
for(Lugar l:lugares){
txtlugar.addItem(l);

}


}
public void limpiarcampo(){



   
    txtid.setText("");
    txtmonto.setText("");

     txtfechacompra.setDate(null);
    txtfechafuncion.setDate(null);
 txtcomprador.setSelectedIndex(-1); 
   
    txtProyeccion.setSelectedIndex(-1); 
    
    txtlugar.removeAllItems(); 
txtactivo.setSelected(false);
    
   




}
